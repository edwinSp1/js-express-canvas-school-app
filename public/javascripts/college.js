function createCollegeLink(college) {
  if(college == 'Harvard University') college = 'Harvard College' //special case
  var dashCase = college.toLowerCase().split(' ').join('-')
  const link = 'https://bigfuture.collegeboard.org/colleges/' + dashCase
  return link
}
